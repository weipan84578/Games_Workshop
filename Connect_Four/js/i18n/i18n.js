  let language = CF.settingsManager.getSettings().language;

  function t(key, params) {
    const table = translations[language] || translations.en;
    const fallback = translations.en[key] || key;
    const template = table[key] || fallback;
    return Object.entries(params || {}).reduce((text, [name, value]) => {
      return text.replace(new RegExp(`\\{${name}\\}`, "g"), value);
    }, template);
  }

  function setLanguage(nextLanguage) {
    language = CF.settingsManager.normalizeLanguage(nextLanguage);
    CF.settingsManager.updateSettings({ language });
    return language;
  }

  function getLanguage() {
    return language;
  }

  function formatNumber(value) {
    return new Intl.NumberFormat(language).format(value);
  }

  CF.i18n = { t, setLanguage, getLanguage, formatNumber, translations };
