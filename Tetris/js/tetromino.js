function getWallKicks(type, fromRot, toRot) {
  const key = `${fromRot}_${toRot}`;
  if (type === 'I') return WALL_KICK_I[key] || [[0,0]];
  if (type === 'O') return [[0,0]];
  return WALL_KICK_JLSTZ[key] || [[0,0]];
}

class Tetromino {
  constructor(type) {
    this.type = type;
    this.color = TETROMINOES[type].color;
    this.rotation = 0;
    this.x = 3;
    this.y = type === 'I' ? -1 : -1;
    this.lastActionWasRotate = false;
  }

  get shape() {
    return TETROMINOES[this.type].shapes[this.rotation];
  }

  clone() {
    const t = new Tetromino(this.type);
    t.rotation = this.rotation; t.x = this.x; t.y = this.y;
    return t;
  }

  getCells() {
    const cells = [];
    const s = this.shape;
    for (let r = 0; r < s.length; r++)
      for (let c = 0; c < s[r].length; c++)
        if (s[r][c]) cells.push({ x: this.x + c, y: this.y + r });
    return cells;
  }
}

// 7-bag randomizer
class Bag {
  constructor() { this.bag = []; }

  next() {
    if (this.bag.length === 0) {
      this.bag = ['I','O','T','S','Z','J','L'];
      for (let i = this.bag.length - 1; i > 0; i--) {
        const j = Math.floor(Math.random() * (i + 1));
        [this.bag[i], this.bag[j]] = [this.bag[j], this.bag[i]];
      }
    }
    return this.bag.shift();
  }
}
